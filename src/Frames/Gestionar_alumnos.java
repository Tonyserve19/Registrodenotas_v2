    public Gestionar_alumnos() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        
        //idcurso = Gestion_cursos.idcurso;
        nombrecurso = Gestion_cursos.nombrecurso;
        //cerrar();
        
        try {
            
            Connection cn2=con.conexion();
            PreparedStatement ps=cn2.prepareStatement("SELECT * FROM alumnos WHERE id_curso_asignado= '"+nombrecurso+"'");
                
            ResultSet rs=ps.executeQuery();
            
            if(rs.next()){
                    
                    setTitle("Alumnos registrados en: "+rs.getString("id_curso_asignado"));
                    lblalumnreg.setText("Alumnos registrados en: "+rs.getString("id_curso_asignado"));
                }
        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null,"Error al cargar titulo");
        }
        
        try {
             
             PreparedStatement ps=cn.prepareStatement("SELECT * FROM alumnos WHERE id_curso_asignado= '"+nombrecurso+"'");
             ResultSet rs=ps.executeQuery();
             
             tabla_gestion_alumnos=new JTable(model);
             jScrollPane1.setViewportView(tabla_gestion_alumnos);
             
             model.addColumn("Id");
             model.addColumn("Nombre");
             model.addColumn("Apellido");
             model.addColumn("Ciclo");
             model.addColumn("Telefono");
             
             while(rs.next()){
                 
                 Object[] fila=new Object[5];
                 for (int i = 0; i < 5; i++) {
                     fila[i]=rs.getObject(i+1);
                     
                 }
                 model.addRow(fila);
             }
             
             cn.close(); 
                
            }  
         catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null,"Error en el llenado de tabla");
        }
        
        ////////////////////////////////////////////////////////////////////////
        
        tabla_gestion_alumnos.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent e){
                
                int fila_point=tabla_gestion_alumnos.rowAtPoint(e.getPoint());
                int columna_point=0;
                
                if(fila_point>-1){
                    
                    idalumno=(int)model.getValueAt(fila_point,columna_point);
                    Informacion_alumnos informacion_alumnos=new Informacion_alumnos();
                    informacion_alumnos.setVisible(true);
                    dispose();
                    
                }
                
            }
        });
        
        
        
}  
